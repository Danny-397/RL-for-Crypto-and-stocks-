/* RL·Trader prototype — renders REAL backtest output from window.RL_RESULTS
   (generated by tools/build_site_data.py). Dependency-free; works under file://
   and GitHub Pages. Falls back gracefully if the data file is missing. */

(() => {
  "use strict";

  const DATA = window.RL_RESULTS || null;
  const VOLT = "#d4ff3f";
  const BENCH = "rgba(140, 152, 168, 0.85)";

  // ── formatting helpers ──────────────────────────────────────
  const sgn = (v) => (v >= 0 ? "+" : "") + (v * 100).toFixed(1) + "%";
  const pct = (v) => (v * 100).toFixed(1) + "%";
  const f2 = (v) => v.toFixed(2);
  const money = (v) => "$" + Math.round(v).toLocaleString("en-US");

  const reduceMotion = window.matchMedia &&
    window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  // ── generic line chart ──────────────────────────────────────
  function lineChart(canvas, series, opts) {
    opts = opts || {};
    const ctx = canvas.getContext("2d");
    const W = canvas.width, H = canvas.height;
    const pad = Object.assign({ l: 10, r: 10, t: 12, b: 12 }, opts.pad);
    ctx.clearRect(0, 0, W, H);

    let lo = Infinity, hi = -Infinity;
    series.forEach((s) => s.data.forEach((v) => { if (v < lo) lo = v; if (v > hi) hi = v; }));
    const span = (hi - lo) || 1;
    lo -= span * 0.08; hi += span * 0.08;

    const X = (i, n) => pad.l + (n <= 1 ? 0 : (i / (n - 1)) * (W - pad.l - pad.r));
    const Y = (v) => pad.t + (1 - (v - lo) / (hi - lo)) * (H - pad.t - pad.b);

    if (opts.grid !== false) {
      ctx.strokeStyle = "rgba(255,255,255,0.05)";
      ctx.lineWidth = 1;
      for (let g = 0; g <= 4; g++) {
        const gy = pad.t + (g / 4) * (H - pad.t - pad.b);
        ctx.beginPath(); ctx.moveTo(pad.l, gy); ctx.lineTo(W - pad.r, gy); ctx.stroke();
      }
    }

    series.forEach((s) => {
      const n = s.data.length;
      const count = s.count !== undefined ? Math.min(s.count, n) : n;
      if (count < 2) return;

      if (s.fill) {
        const grad = ctx.createLinearGradient(0, pad.t, 0, H);
        grad.addColorStop(0, "rgba(212,255,63,0.16)");
        grad.addColorStop(1, "rgba(212,255,63,0)");
        ctx.beginPath();
        ctx.moveTo(X(0, n), Y(s.data[0]));
        for (let i = 1; i < count; i++) ctx.lineTo(X(i, n), Y(s.data[i]));
        ctx.lineTo(X(count - 1, n), H - pad.b);
        ctx.lineTo(X(0, n), H - pad.b);
        ctx.closePath(); ctx.fillStyle = grad; ctx.fill();
      }

      ctx.beginPath();
      ctx.moveTo(X(0, n), Y(s.data[0]));
      for (let i = 1; i < count; i++) ctx.lineTo(X(i, n), Y(s.data[i]));
      ctx.strokeStyle = s.color; ctx.lineWidth = s.width || 2; ctx.lineJoin = "round";
      if (s.glow) { ctx.save(); ctx.shadowColor = s.color; ctx.shadowBlur = 10; ctx.stroke(); ctx.restore(); }
      else ctx.stroke();

      if (s.head) {
        const cx = X(count - 1, n), cy = Y(s.data[count - 1]);
        ctx.save(); ctx.fillStyle = s.color; ctx.shadowColor = s.color; ctx.shadowBlur = 12;
        ctx.beginPath(); ctx.arc(cx, cy, 3.6, 0, Math.PI * 2); ctx.fill(); ctx.restore();
      }
    });
  }

  // ── drawdown area chart ─────────────────────────────────────
  function drawdownChart(canvas, dd) {
    const ctx = canvas.getContext("2d");
    const W = canvas.width, H = canvas.height;
    const pad = { l: 10, r: 10, t: 10, b: 10 };
    ctx.clearRect(0, 0, W, H);
    const maxdd = Math.max(0.0001, Math.max.apply(null, dd));
    const X = (i) => pad.l + (i / (dd.length - 1)) * (W - pad.l - pad.r);
    const Y = (v) => pad.t + (v / maxdd) * (H - pad.t - pad.b);

    ctx.beginPath();
    ctx.moveTo(X(0), pad.t);
    for (let i = 0; i < dd.length; i++) ctx.lineTo(X(i), Y(dd[i]));
    ctx.lineTo(X(dd.length - 1), pad.t); ctx.closePath();
    const grad = ctx.createLinearGradient(0, pad.t, 0, H);
    grad.addColorStop(0, "rgba(255,107,107,0.05)");
    grad.addColorStop(1, "rgba(255,107,107,0.4)");
    ctx.fillStyle = grad; ctx.fill();

    ctx.beginPath();
    for (let i = 0; i < dd.length; i++) (i ? ctx.lineTo(X(i), Y(dd[i])) : ctx.moveTo(X(i), Y(dd[i])));
    ctx.strokeStyle = "rgba(255,107,107,0.85)"; ctx.lineWidth = 1.5; ctx.stroke();
  }

  // ── action histogram ────────────────────────────────────────
  function histogram(canvas, actions) {
    const ctx = canvas.getContext("2d");
    const W = canvas.width, H = canvas.height;
    const pad = { l: 8, r: 8, t: 10, b: 20 };
    const BINS = 21, lo = -1, hi = 1;
    ctx.clearRect(0, 0, W, H);

    const counts = new Array(BINS).fill(0);
    actions.forEach((a) => {
      let b = Math.floor(((a - lo) / (hi - lo)) * BINS);
      b = Math.max(0, Math.min(BINS - 1, b));
      counts[b]++;
    });
    const maxc = Math.max(1, Math.max.apply(null, counts));
    const bw = (W - pad.l - pad.r) / BINS;

    counts.forEach((c, i) => {
      const h = (c / maxc) * (H - pad.t - pad.b);
      const x = pad.l + i * bw, y = H - pad.b - h;
      const center = i / (BINS - 1); // 0..1
      ctx.fillStyle = center < 0.45 ? "rgba(255,107,107,0.7)"
        : center > 0.55 ? "rgba(212,255,63,0.85)" : "rgba(140,152,168,0.6)";
      ctx.fillRect(x + 1, y, bw - 2, h);
    });

    const zx = pad.l + ((-lo) / (hi - lo)) * (W - pad.l - pad.r);
    ctx.strokeStyle = "rgba(255,255,255,0.2)"; ctx.setLineDash([3, 3]);
    ctx.beginPath(); ctx.moveTo(zx, pad.t); ctx.lineTo(zx, H - pad.b); ctx.stroke(); ctx.setLineDash([]);

    ctx.fillStyle = "rgba(255,255,255,0.42)"; ctx.font = "10px JetBrains Mono, monospace";
    ctx.textAlign = "center";
    ctx.fillText("short", pad.l + bw * 2.2, H - 5);
    ctx.fillText("flat", zx, H - 5);
    ctx.fillText("long", W - pad.r - bw * 2.2, H - 5);
  }

  // ── scorecard ───────────────────────────────────────────────
  function renderScorecard(m) {
    const el = document.getElementById("scorecard");
    if (!el) return;
    const a = m.metrics, b = m.bench_metrics;
    const rows = [
      ["Total return", sgn(a.total_return), sgn(b.total_return), a.total_return >= b.total_return],
      ["Sharpe", f2(a.sharpe), f2(b.sharpe), a.sharpe >= b.sharpe],
      ["Sortino", f2(a.sortino), "—", null],
      ["Max drawdown", pct(a.max_drawdown), pct(b.max_drawdown), a.max_drawdown <= b.max_drawdown],
      ["Final equity", money(a.final_equity), money(b.final_equity), a.final_equity >= b.final_equity],
    ];
    if (m.win_rate !== undefined) {
      rows.push(["Win rate vs B&H", (m.win_rate * 100).toFixed(0) + "%", "—", m.win_rate >= 0.5]);
    }
    el.innerHTML = rows.map(([k, av, bv, win]) => `
      <div class="sc-row">
        <span class="sc-k">${k}</span>
        <span class="sc-a ${win === true ? "win" : win === false ? "lose" : ""}">${av}</span>
        <span class="sc-b">${bv}</span>
      </div>`).join("");
  }

  // ── render one market into the dashboard ────────────────────
  function renderMarket(market) {
    if (!DATA) return;
    const m = DATA.markets[market];
    if (!m) return;
    lineChart(document.getElementById("dashEquity"), [
      { data: m.equity_bench, color: BENCH, width: 2 },
      { data: m.equity_agent, color: VOLT, width: 2.6, glow: true, fill: true, head: true },
    ]);
    drawdownChart(document.getElementById("dashDD"), m.drawdown);
    histogram(document.getElementById("dashActions"), m.actions);
    renderScorecard(m);
  }

  // ── hero chart (animated reveal of real stock data) ─────────
  function initHero() {
    const canvas = document.getElementById("equityChart");
    if (!canvas) return;

    let agent, bench;
    if (DATA && DATA.markets.stock) {
      agent = DATA.markets.stock.equity_agent;
      bench = DATA.markets.stock.equity_bench;
    } else {
      // Fallback synthetic curve if results.js is absent.
      agent = Array.from({ length: 80 }, (_, i) => 1 + i * 0.004 + Math.sin(i / 6) * 0.02);
      bench = Array.from({ length: 80 }, (_, i) => 1 + i * 0.002 + Math.sin(i / 5) * 0.03);
    }
    const N = Math.max(agent.length, bench.length);
    let count = reduceMotion ? N : 2;

    function frame() {
      lineChart(canvas, [
        { data: bench, color: BENCH, width: 2, count: count },
        { data: agent, color: VOLT, width: 2.6, glow: true, fill: true, head: true, count: count },
      ]);
      if (count < N) { count += Math.max(1, Math.round(N / 90)); requestAnimationFrame(frame); }
      else revealHeroMetrics();
    }

    let done = false;
    function revealHeroMetrics() {
      if (done || !DATA) return;
      done = true;
      const m = DATA.markets.stock.metrics;
      countTo("m-return", 0, m.total_return * 100, 800, (v) => (v >= 0 ? "+" : "") + v.toFixed(1) + "%");
      countTo("m-sharpe", 0, m.sharpe, 800, (v) => v.toFixed(2));
      countTo("m-dd", 0, m.max_drawdown * 100, 800, (v) => v.toFixed(1) + "%");
    }

    if ("IntersectionObserver" in window) {
      const io = new IntersectionObserver((e) => {
        if (e[0].isIntersecting) { frame(); io.disconnect(); }
      }, { threshold: 0.25 });
      io.observe(canvas);
    } else { frame(); }
  }

  // ── count-up helper ─────────────────────────────────────────
  function countTo(id, from, to, dur, fmt) {
    const el = document.getElementById(id);
    if (!el) return;
    if (reduceMotion) { el.textContent = fmt(to); return; }
    const start = performance.now();
    (function tick(now) {
      const p = Math.min(1, (now - start) / dur);
      const eased = 1 - Math.pow(1 - p, 3);
      el.textContent = fmt(from + (to - from) * eased);
      if (p < 1) requestAnimationFrame(tick);
    })(start);
  }

  // ── stats strip count-up ────────────────────────────────────
  function initStats() {
    const nums = document.querySelectorAll(".stat-cell .num");
    if (!nums.length || !("IntersectionObserver" in window)) return;
    const io = new IntersectionObserver((entries, obs) => {
      entries.forEach((e) => {
        if (!e.isIntersecting) return;
        const el = e.target, target = parseInt(el.dataset.count, 10);
        const suffix = el.querySelector(".pct");
        let i = 0; const step = Math.max(1, Math.round(target / 28));
        const timer = setInterval(() => {
          i = Math.min(target, i + step);
          el.childNodes[0].nodeValue = String(i);
          if (suffix) el.appendChild(suffix);
          if (i >= target) clearInterval(timer);
        }, 28);
        obs.unobserve(el);
      });
    }, { threshold: 0.5 });
    nums.forEach((n) => io.observe(n));
  }

  // ── data note + market toggle ───────────────────────────────
  function initDashboard() {
    if (!DATA) return;
    const note = document.getElementById("data-note");
    if (note) {
      const nEval = (DATA.markets.stock && DATA.markets.stock.n_eval) || null;
      const evalStr = nEval ? ` · mean over ${nEval} held-out paths` : "";
      note.textContent =
        `Trained on ${DATA.data_source} · ${DATA.timesteps.toLocaleString()} steps${evalStr} · ${DATA.generated}.`;
    }
    const toggles = document.querySelectorAll(".tg");
    toggles.forEach((btn) => {
      btn.addEventListener("click", () => {
        toggles.forEach((b) => b.classList.remove("active"));
        btn.classList.add("active");
        renderMarket(btn.dataset.market);
      });
    });
    // Render the default market once the dashboard scrolls into view.
    const dash = document.querySelector(".dash-grid");
    if (dash && "IntersectionObserver" in window) {
      const io = new IntersectionObserver((e) => {
        if (e[0].isIntersecting) { renderMarket("stock"); io.disconnect(); }
      }, { threshold: 0.15 });
      io.observe(dash);
    } else {
      renderMarket("stock");
    }
  }

  // ── live inference widget (optional Render backend) ─────────
  function initLive() {
    const API = (window.RL_API || "").replace(/\/+$/, "");
    const section = document.getElementById("live");
    if (!API || !section) return; // stays hidden when no backend is configured
    section.hidden = false;

    const marketSel = document.getElementById("live-market");
    const tickerSel = document.getElementById("live-ticker");
    const runBtn = document.getElementById("live-run");
    const statusEl = document.getElementById("live-status");
    const signalEl = document.getElementById("live-signal");
    const scoreEl = document.getElementById("live-scorecard");
    const canvas = document.getElementById("live-chart");

    const FALLBACK = {
      stock: ["AAPL", "MSFT", "GOOGL", "AMZN", "NVDA", "SPY", "QQQ"],
      crypto: ["BTC-USD", "ETH-USD", "SOL-USD", "XRP-USD", "LTC-USD"],
    };
    let tickerMap = FALLBACK;

    function fillTickers() {
      const list = tickerMap[marketSel.value] || FALLBACK[marketSel.value] || [];
      tickerSel.innerHTML = list.map((t) => `<option>${t}</option>`).join("");
    }
    fillTickers();
    marketSel.addEventListener("change", fillTickers);
    fetch(`${API}/api/tickers`).then((r) => r.json())
      .then((m) => { tickerMap = m; fillTickers(); }).catch(() => {});

    function signalText(a) {
      if (Math.abs(a) < 0.05) return `Agent's current call: <span class="sig-flat">FLAT</span> (cash)`;
      const cls = a > 0 ? "sig-long" : "sig-short";
      return `Agent's current call: <span class="${cls}">${Math.round(Math.abs(a) * 100)}% ${a > 0 ? "LONG" : "SHORT"}</span>`;
    }

    function liveScorecard(d) {
      const a = d.metrics, b = d.bench_metrics;
      const rows = [
        ["Total return", sgn(a.total_return), sgn(b.total_return), a.total_return >= b.total_return],
        ["Sharpe", f2(a.sharpe), f2(b.sharpe), a.sharpe >= b.sharpe],
        ["Max drawdown", pct(a.max_drawdown), pct(b.max_drawdown), a.max_drawdown <= b.max_drawdown],
      ];
      scoreEl.innerHTML = rows.map(([k, av, bv, win]) => `
        <div class="sc-row"><span class="sc-k">${k}</span>
        <span class="sc-a ${win ? "win" : "lose"}">${av}</span>
        <span class="sc-b">${bv}</span></div>`).join("");
    }

    async function run() {
      runBtn.disabled = true;
      statusEl.textContent = "fetching prices + running agent… (first call can take ~30s on a cold backend)";
      signalEl.innerHTML = ""; scoreEl.innerHTML = "";
      try {
        const r = await fetch(`${API}/api/live?market=${marketSel.value}&ticker=${tickerSel.value}`);
        const d = await r.json();
        if (d.error) throw new Error(d.error);
        signalEl.innerHTML = signalText(d.latest_action);
        lineChart(canvas, [
          { data: d.equity_bench, color: BENCH, width: 2 },
          { data: d.equity_agent, color: VOLT, width: 2.6, glow: true, fill: true, head: true },
        ]);
        liveScorecard(d);
        statusEl.textContent = `${d.ticker} · ${d.n_days} trading days`;
      } catch (e) {
        statusEl.textContent = "⚠ " + (e && e.message ? e.message : "request failed");
      } finally {
        runBtn.disabled = false;
      }
    }
    runBtn.addEventListener("click", run);
  }

  // ── boot ────────────────────────────────────────────────────
  initHero();
  initStats();
  initDashboard();
  initLive();
})();
