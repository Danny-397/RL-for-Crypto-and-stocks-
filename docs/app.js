  // ── live inference widget (optional Render backend) ─────────
  function initLive() {
    const API = (window.RL_API || "").replace(/\/+$/, "");
    const section = document.getElementById("live");
    if (!API || !section) return; // stays hidden when no backend is configured
    section.hidden = false;

    const marketSel = document.getElementById("live-market");
    const tickerSel = document.getElementById("live-ticker");
    const runBtn = document.getElementById("live-run");
    const statusEl = document.getElementById("live-status");
    const signalEl = document.getElementById("live-signal");
    const scoreEl = document.getElementById("live-scorecard");
    const canvas = document.getElementById("live-chart");
    const spinEl = document.getElementById("live-spin");

    // pre-warm the free-tier backend so the first real run isn't a cold start
    fetch(`${API}/health`).catch(() => {});

    const FALLBACK = {
      stock: ["AAPL", "MSFT", "GOOGL", "AMZN", "NVDA", "SPY", "QQQ"],
      crypto: ["BTC-USD", "ETH-USD", "SOL-USD", "XRP-USD", "LTC-USD"],
    };
    let tickerMap = FALLBACK;

    const datalist = document.getElementById("ticker-suggestions");
    function fillTickers(setDefault) {
      const list = tickerMap[marketSel.value] || FALLBACK[marketSel.value] || [];
      if (datalist) datalist.innerHTML = list.map((t) => `<option value="${t}"></option>`).join("");
      if (setDefault) tickerSel.value = list[0] || "";
    }
    fillTickers(true);
    // changing market just refreshes suggestions + the default symbol
    marketSel.addEventListener("change", () => fillTickers(true));
    fetch(`${API}/api/tickers`).then((r) => r.json())
      .then((m) => { tickerMap = m; fillTickers(!tickerSel.value); }).catch(() => {});

    // accept ANY symbol the user types; normalise crypto to yfinance's FOO-USD form
    function normalizedTicker() {
      let t = (tickerSel.value || "").trim().toUpperCase();
      if (marketSel.value === "crypto" && t && !t.includes("-")) t += "-USD";
      return t;
    }

    function signalText(a) {
      if (Math.abs(a) < 0.05) return `Agent's current call: <span class="sig-flat">FLAT</span> (cash)`;
      const cls = a > 0 ? "sig-long" : "sig-short";
      return `Agent's current call: <span class="${cls}">${Math.round(Math.abs(a) * 100)}% ${a > 0 ? "LONG" : "SHORT"}</span>`;
    }

    function liveScorecard(d) {
      const a = d.metrics, b = d.bench_metrics;
      const rows = [
        ["Total return", sgn(a.total_return), sgn(b.total_return), a.total_return >= b.total_return],
        ["Sharpe", f2(a.sharpe), f2(b.sharpe), a.sharpe >= b.sharpe],
        ["Max drawdown", pct(a.max_drawdown), pct(b.max_drawdown), a.max_drawdown <= b.max_drawdown],
      ];
      scoreEl.innerHTML = rows.map(([k, av, bv, win]) => `
        <div class="sc-row"><span class="sc-k">${k}</span>
        <span class="sc-a ${win ? "win" : "lose"}">${av}</span>
        <span class="sc-b">${bv}</span></div>`).join("");
    }

    async function run() {
      const ticker = normalizedTicker();
      if (!ticker) { statusEl.textContent = "Type a ticker symbol first (e.g. TSLA or DOGE-USD)."; return; }
      tickerSel.value = ticker;  // show the normalised symbol back to the user
      runBtn.disabled = true;
      if (spinEl) spinEl.hidden = false;
      statusEl.textContent = `fetching ${ticker} + running agent… (first call can take ~30s on a cold backend)`;
      signalEl.innerHTML = ""; scoreEl.innerHTML = "";
      try {
        const r = await fetch(`${API}/api/live?market=${marketSel.value}&ticker=${encodeURIComponent(ticker)}`);
        const d = await r.json();
        if (d.error) throw new Error(d.error);
        signalEl.innerHTML = signalText(d.latest_action);
        lineChart(canvas, [
          { data: d.equity_bench, color: BENCH, width: 2 },
          { data: d.equity_agent, color: VOLT, width: 2.6, glow: true, fill: true, head: true },
        ]);
        liveScorecard(d);
        statusEl.textContent = `${d.ticker} · ${d.n_days} trading days · held-out 2y`;
      } catch (e) {
        statusEl.textContent = "Error: " + (e && e.message ? e.message : "request failed");
      } finally {
        runBtn.disabled = false;
        if (spinEl) spinEl.hidden = true;
      }
    }
    runBtn.addEventListener("click", run);
    tickerSel.addEventListener("keydown", (e) => { if (e.key === "Enter") { e.preventDefault(); run(); } });
  }

  // ── interactive lab: learning curve + market switch ─────────
  function ema(arr, alpha) {
    if (!arr.length) return arr;
    let m = arr[0]; const out = [];
    for (const v of arr) { m = alpha * v + (1 - alpha) * m; out.push(m); }
    return out;
  }

  function renderLab(market) {
    if (!DATA) return;
    const m = DATA.markets[market];
    if (!m || !m.training || !m.training.reward) return;
    const r = m.training.reward;
    lineChart(document.getElementById("labLearn"), [
      { data: r, color: "rgba(140,152,168,0.45)", width: 1 },
      { data: ema(r, 0.15), color: VOLT, width: 2.6, glow: true },
    ]);
    const cap = document.getElementById("labCaption");
    if (cap) {
      const k = Math.min(5, r.length);
      const mean = (a) => a.reduce((x, y) => x + y, 0) / a.length;
      const first = mean(r.slice(0, k)), last = mean(r.slice(-k));
      const verb = last > first ? "climbed" : "moved";
      cap.innerHTML =
        `Mean episode reward ${verb} from <b>${first.toFixed(1)}</b> to ` +
        `<b>${last.toFixed(1)}</b> over ${r.length} PPO updates — the policy is ` +
        `learning a strategy. (Reward is the env's risk-adjusted objective, not dollars; ` +
        `whether that translates to real-market profit is the honest question answered above.)`;
    }
  }

